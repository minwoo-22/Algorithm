package baekjoon.io;

public class Sprout {
    public static void main(String[] args) {
        String[] strs = {
          "         ,r'\"7", "r`-_   ,'  ,/", " \\. \". L_r'"
          , "   `~\\/", "      |", "      |"
        };
        
        for (int i = 0; i < strs.length; i++) {
            System.out.println(strs[i]);
        }
    }
}
